    private void setForm(NhaXuatBan nxb) {
        this.txtTenNXB2.setText(nxb.getTenNhaXB());
        this.txtDiachiNXB.setText(nxb.getDiaChi());
        this.txtSDTNXB.setText(nxb.getSDT());
        if (nxb.isTrangThai() == 1) {
            rdoHDNXB.setSelected(true);
        } else {
            rdoUnHDNXB.setSelected(true);
        }
    }

    private void setFormNCC(NhaCungCap ncc) {
        this.txtTenNCC.setText(ncc.getTenNhaCP());
        this.txtDiachiNCC.setText(ncc.getDiaChi());
        this.txtSDTNCC.setText(ncc.getSDT());
        if (ncc.isTrangThai() == 1) {
            rdoHDNCC.setSelected(true);
        } else {
            rdoUnHDNCC.setSelected(true);
        }
    }

    private NhaXuatBan getFormNXB() {
        NhaXuatBan nxb = new NhaXuatBan();
        nxb.setTenNhaXB(this.txtTenNXB2.getText());
        nxb.setDiaChi(this.txtDiachiNXB.getText());
        nxb.setSDT(this.txtSDTNXB.getText());
        nxb.setTrangThai(this.rdoHDNXB.isSelected() ? 1 : 0);
        return nxb;
    }

    private NhaCungCap getFormNCC() {
        NhaCungCap ncc = new NhaCungCap();
        ncc.setTenNhaCP(this.txtTenNCC.getText());
        ncc.setDiaChi(this.txtDiachiNCC.getText());
        ncc.setSDT(this.txtSDTNCC.getText());
        ncc.setTrangThai(this.rdoHDNCC.isSelected() ? 1 : 0);
        return ncc;
    }

    private void showInforNXB() {
        String TenNXB = (String) tblNXB.getValueAt(this.index, 0);
        NhaXuatBan nxb = daoNXB.selectById(TenNXB);
        this.setForm(nxb);
    }

    private void showInforNCC() {
        String TenNCC = (String) tblNCC2.getValueAt(this.index, 0);
        NhaCungCap ncc = dao2.selectById(TenNCC);
        this.setFormNCC(ncc);
    }

